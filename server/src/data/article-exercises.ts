export type ArticleAnswer = 'a' | 'an' | 'the' | '—';

export type ArticleBlank = {
  position: number;
  correctAnswer: ArticleAnswer;
  explanation: string;
};

export type ArticleExercise = {
  sentence: string;
  blanks: ArticleBlank[];
  difficulty: 1 | 2 | 3;
  rule: string;
  ruleCategory: RuleCategory;
};

function blank(sentence: string, correctAnswer: ArticleAnswer, explanation: string): ArticleBlank {
  const position = sentence.indexOf('___');
  return { position, correctAnswer, explanation };
}

